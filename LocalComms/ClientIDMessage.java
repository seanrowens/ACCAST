/*
 * ClientIDMessage.java
 *
 * A client of LocalComms should send this message to the server after connection
 * The string contained in the message identifies the client for the purposes
 * of other clients.
 *
 * Created on 13 May 2003, 10:03
 */

package LocalComms;

import java.io.Serializable;

/**
 *
 * @author  scerri
 */
public class ClientIDMessage implements Serializable {
    
    /** Creates a new instance of ClientIDMessage */
    public ClientIDMessage(String id) {
        this.id = id;
    }
    
    public String id = null;
    
    public static final long serialVersionUID = 1L;
}
