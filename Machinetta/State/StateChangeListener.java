package Machinetta.State;

/**
 * Objects that want to be notified when state changes will 
 * implement this interface and register with State.
 *
 * @author  scerri
 */
public interface StateChangeListener {
    
    /** 
     * Called when something updates state 
     * 
     * @param b array of BeliefIDs that have changed.
     */
    public void stateChanged (BeliefID [] b); 
    
}
